type DictionaryKey = keyof typeof dictionaries.ko;
type Params = Record<string, string | number>;

type I18nContextValue = {
  language: Language;
  setLanguage: (language: Language) => void;
  t: (key: DictionaryKey, params?: Params) => string;
  emotionLabel: (tag: EmotionTag) => string;
};

const I18nContext = createContext<I18nContextValue | null>(null);

export function LanguageProvider({ children }: { children: React.ReactNode }) {
  const [language, setLanguageState] = useState<Language>('ko');

  useEffect(() => {
    const saved = window.localStorage.getItem(LANGUAGE_KEY);
    if (saved === 'ko' || saved === 'en' || saved === 'ja') setLanguageState(saved);
  }, []);

  function setLanguage(next: Language) {
    setLanguageState(next);
    window.localStorage.setItem(LANGUAGE_KEY, next);
    document.documentElement.lang = next;
  }

  const value = useMemo<I18nContextValue>(() => {
    function translate(key: DictionaryKey, params?: Params) {
      let text: string = dictionaries[language][key] ?? dictionaries.ko[key] ?? key;
      if (params) {
        for (const [name, value] of Object.entries(params)) {
          text = text.replaceAll(`{${name}}`, String(value));
        }
      }
      return text;
    }

    return {
      language,
      setLanguage,
      t: translate,
      emotionLabel(tag) {
        // 레거시 키(즐거움/분노 등)도 새 척도로 해석해 라벨을 찾는다.
        const resolved = resolveEmotion(tag)?.tag ?? tag;
        return (emotionLabels[language] as Record<string, string>)[resolved] ?? tag;
      },
    };
  }, [language]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const value = useContext(I18nContext);
  if (!value) throw new Error('useI18n must be used inside LanguageProvider');
  return value;
}
